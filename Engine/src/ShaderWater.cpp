#include "ShaderWater.h"

bool ShaderWater::CreateShader()
{
    std::string vert =
        "#version 460 core\n"
        "layout(location = 0) in vec3 aPos;\n"
        "out vec4 ScreenPos;\n"
        "out vec3 WorldPos;\n"
        "uniform mat4 model;\n"
        "uniform mat4 view;\n"
        "uniform mat4 projection;\n"
        "void main() {\n"
        "    vec4 wPos = model * vec4(aPos, 1.0);\n"
        "    WorldPos = wPos.xyz;\n"
        "    vec4 clipPos = projection * view * wPos;\n"
        "    ScreenPos = clipPos;\n"
        "    gl_Position = clipPos;\n"
        "}\n";

    std::string frag =
        "#version 460 core\n"
        "out vec4 FragColor;\n"
        "in vec4 ScreenPos;\n"
        "in vec3 WorldPos;\n"
        "uniform sampler2D uSceneDepthMap;\n"
        "uniform mat4 uInverseVP;\n"
        "uniform float uTime;\n"
        "uniform vec3 uWaterColor;\n"
        "uniform vec3 uFoamColor;\n"
        "uniform float uFoamThickness;\n"
        "uniform float uFoamOffset;\n"
        "\n"
        "// Función para sacar la posición XYZ real del terreno que hay bajo el agua\n"
        "vec3 GetWorldPosFromDepth(vec2 ndc, float rawDepth) {\n"
        "    vec4 clipSpace = vec4(ndc * 2.0 - 1.0, rawDepth * 2.0 - 1.0, 1.0);\n"
        "    vec4 worldSpace = uInverseVP * clipSpace;\n"
        "    return worldSpace.xyz / worldSpace.w;\n"
        "}\n"
        "\n"
        "void main() {\n"
        "    vec2 ndc = (ScreenPos.xy / ScreenPos.w) * 0.5 + 0.5;\n"
        "    float rawDepth = texture(uSceneDepthMap, ndc).r;\n"
        "\n"
        "    // Coordenadas XYZ del fondo del mar\n"
        "    vec3 terrainWorldPos = GetWorldPosFromDepth(ndc, rawDepth);\n"
        "\n"
        "    // DISTANCIA VERTICAL (Totalmente estática e independiente de la cámara)\n"
        "    float verticalDepth = WorldPos.y - terrainWorldPos.y;\n"
        "\n"
        "    // --- MAGIA DE ANIMACIÓN (SOLO MAREA, LÍNEA RECTA) --- \n"
        "    // Marea Global: hace que todo el anillo respire/suba y baje lentamente\n"
        "    // Cambia el 1.5 para la velocidad y el 0.05 para lo mucho que se desplaza\n"
        "    float tide = sin(uTime * 1.5) * 0.05;\n"
        "\n"
        "    // Sumamos la marea a tu hueco original (sin deformar la forma)\n"
        "    float dynamicOffset = uFoamOffset + tide;\n"
        "    // ---------------------------------------------------- \n"
        "\n"
        "    // isFoam dará 1.0 solo si la profundidad vertical está entre el Offset dinámico y el Grosor\n"
        "    float isFoam = step(dynamicOffset, verticalDepth) - step(dynamicOffset + uFoamThickness, verticalDepth);\n"
        "\n"
        "    vec3 finalColor = mix(uWaterColor, uFoamColor, isFoam);\n"
        "    FragColor = vec4(finalColor, 1.0);\n"
        "}\n";

    return LoadFromSource(vert.c_str(), frag.c_str());
}